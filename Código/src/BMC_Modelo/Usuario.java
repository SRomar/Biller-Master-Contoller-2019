package BMC_Modelo;



public class Usuario {

	private int dni;
	private String nombre;
	private String apellido;
	private String usuario;
	private String contrasenia;
	private String domicilio;
	private String telefono;
	private String tipo;
	private String foto;
	private int cantidadProductosRegistrados;

	
	
	public int getCantidadProductosRegistrados() {
		return cantidadProductosRegistrados;
	}

	public void setCantidadProductosRegistrados(int cantidadProductosRegistrados) {
		this.cantidadProductosRegistrados = cantidadProductosRegistrados;
	}
	public int getDni() {
		return dni;
	}
	public void setDni(int dni) {
		this.dni = dni;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getApellido() {
		return apellido;
	}
	public void setApellido(String apellido) {
		this.apellido = apellido;
	}
	public String getUsuario() {
		return usuario;
	}
	public void setUsuario(String usuario) {
		this.usuario = usuario;
	}
	public String getContrasenia() {
		return contrasenia;
	}
	public void setContrasenia(String contrasenia) {
		this.contrasenia = contrasenia;
	}
	public String getDomicilio() {
		return domicilio;
	}
	public void setDomicilio(String domicilio) {
		this.domicilio = domicilio;
	}
	public String getTelefono() {
		return telefono;
	}
	public void setTelefono(String telefono) {
		this.telefono = telefono;
	}
	public String getTipo() {
		return tipo;
	}
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}
	public String getFoto() {
		return foto;
	}
	public void setFoto(String foto) {
		this.foto = foto;
	}
	
	
	
	
	
	
	
	

}

	

	
	
	
	
